class WalkAnimeThd extends Thread
{
    private int coordX;
    private int coordY;
    
    public WalkAnimeThd()
    {
        this.coordX = 130;
        this.coordY = 470;
    }
    
    public void run()
    {
        try {
            Thread.sleep(1000);
        } catch (InterruptedException ex) {
            Logger.getLogger(WalkAnimeThd.class.getName()).log(Level.SEVERE, null, ex);
        } 
        
        while (this.coordX <= 600)
        {
            try {
                Thread.sleep(80);
            } catch (InterruptedException ex) {
                Logger.getLogger(WalkAnimeThd.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            this.coordX += 10;
            
            turnWalking();
        }
    }

    private void turnWalking()
    {
        SwingUtilities.invokeLater(new Runnable() 
        {
            public void run() 
            {
                EndingPanel.getInstance().setWalkPosition(coordX, coordY);
            }
        });
    }
}

class raiseAnimeThd extends Thread
{
    private int coordX;
    private int coordY;
    
    public raiseAnimeThd()
    {
        this.coordX = 610;
        this.coordY = 470;
    }
    
    public void run()
    {
        try {
            Thread.sleep(5000);
        } catch (InterruptedException ex) {
            Logger.getLogger(raiseAnimeThd.class.getName()).log(Level.SEVERE, null, ex);
        } 
        turnPicking();
        
        while (this.coordY >= 400)
        {
            try {
                Thread.sleep(30);
            } catch (InterruptedException ex) {
                Logger.getLogger(raiseAnimeThd.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            this.coordY -= 3;
            
            turnPicking();
        }
        
    }
    
    private void turnPicking()
    {
        SwingUtilities.invokeLater(new Runnable() 
        {
            public void run() 
            {
                EndingPanel.getInstance().setCubePosition(coordX, coordY);
            }
        });
    }
}
